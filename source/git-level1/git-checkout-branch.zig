title: What are branches?
points:
- Branches are temporary collections of changes that might eventually be merged back into the main stream of work.
- There are lots of branching strategies to accommodate many developers working on many different work streams.
- We'll use branches sometimes as a "before" and "after", or for solutions to challenges.
- The primary branch is usually called "master" (but that's not a requirement).
- You checkout or "switch" branches.
