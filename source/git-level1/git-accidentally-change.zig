-----bullets
title: Making changes in git
points:
- Each file is either "tracked" or "untracked"
- Once you tell git about it a file, it will watch everything you do with that file
- If you make a change, it will expect you to create a "commit".
- A commit is a change to one or more files that you want to share with others.

-----bullets
title: What if I don't want to share my change?
points:
- git expects you to make a commit
- If you don't want to, you can either:
- stash
- reset
